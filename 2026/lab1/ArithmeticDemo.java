public class ArithmeticDemo {
    public static void main(String[] args) {
        int result =1;
        

        System.out.println(result+=2);
        System.out.println(result-=1);
        System.out.println(result*=2);
        System.out.println(result/=2);
        System.out.println(result+=8);
        System.out.println(result%=7);



    }
    
}
