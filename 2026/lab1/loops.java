public class loops {
    public static void main(String[] args) {
        int i = 1;
        while (i<=10) {
            System.err.println(i+". While loop");
            i++;
            
        }
        System.err.println();
        for(i=1;i<=10; i++){
           System.err.println(i+". For loop");  
        }

         System.err.println();

        for(int j=1;j<=3; i++){
           System.err.println(i+". For loop");  
        }
        System.err.println();

        for(int j=1;j<=3; i++){
           System.err.println(i+ ". For loop");  
        }
    }
    
}
