public class PrimTypes {
    public static void main(String[] args) {
        boolean flag = true;
        char c = 'A';
        byte b = 100;
        short s = 32000;
        int i = 2_000_000;
        long l = 3_000_000_000L;
        float f = 32.85F;
        double d = 100.000;

        System.out.println("Boolean:" +  flag + "\nChar:" + c + 
                           "byte:" +  b + "\nshort:" + s + 
                           "\nint:" +  i + "\nlong:" + l + 
                           "\nfloat:" +  f + "\ndouble:" + d  ) ;


    }
}